package com.fenix.wakonga.model;

public class Servico {
    private String nome, imagem, descricao, preco, desconto, menuId, telefone, email, provincia, municipio, bairro;

    public Servico() {
    }

    public Servico(String nome, String imagem, String descricao, String preco, String desconto, String menuId, String telefone, String email, String provincia, String municipio, String bairro) {
        this.nome = nome;
        this.imagem = imagem;
        this.descricao = descricao;
        this.preco = preco;
        this.desconto = desconto;
        this.menuId = menuId;
        this.telefone = telefone;
        this.email = email;
        this.provincia = provincia;
        this.municipio = municipio;
        this.bairro = bairro;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getImagem() {
        return imagem;
    }

    public void setImagem(String imagem) {
        this.imagem = imagem;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getPreco() {
        return preco;
    }

    public void setPreco(String preco) {
        this.preco = preco;
    }

    public String getDesconto() {
        return desconto;
    }

    public void setDesconto(String desconto) {
        this.desconto = desconto;
    }

    public String getMenuId() {
        return menuId;
    }

    public void setMenuId(String menuId) {
        this.menuId = menuId;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getProvincia() {
        return provincia;
    }

    public void setProvincia(String provincia) {
        this.provincia = provincia;
    }

    public String getMunicipio() {
        return municipio;
    }

    public void setMunicipio(String municipio) {
        this.municipio = municipio;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }
}

